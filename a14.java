//switch case
class a14{

	public static void swtch(int ch){

		switch(ch){
			
			case 1:
			
				System.out.println("You chose case 1");
				break;
			case 2:
			
				System.out.println("You chose case 2");
				break;
			case 3:
			
				System.out.println("You chose case 3");
				break;
			default:
			
				System.out.println("You chose default case");
				break;		
		}
	}
	
	public static void main(String []a){
	
		swtch(2);
	
	}
	

}
